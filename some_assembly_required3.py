def encode(char, index):
	l = arr_1067[4 - index % 5]
	n = ctypes.c_int32(l << 24).value
	o = ctypes.c_int32(n >> 24).value
	q = ctypes.c_int32(ord(char) ^ o).value
	ret = ctypes.c_uint8(q).value
	return ret

charset = string.printable

